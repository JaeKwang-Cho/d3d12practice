// 여기서는 diffuse, albedo, roughness 밖에 없지만,
// 예쁘게 하려면 엄청 뭐가 많다고 한다.
struct Material
{
	// 이름
	std::string Name;

	// Constant Buffer에서 offset 위치이다.
	int MatCBIndex = -1;

	// Shader Resourse View Heap에서 Diffuse Texture의 Offset 위치이다.
	int DiffuseSrvHeapIndex = -1;

	// Shader Resourse View Heap에서 Normal Texture의 Offset 위치이다.
	int NormalSrvHeapIndex = -1;

	// 이 친구도 Dirty flag 를 가지고 있는다.
	int NumFramesDirty = g_NumFrameResources;

	// CB로 넘어가는 Material 값들이다.
	DirectX::XMFLOAT4 DiffuseAlbedo = { 1.0f, 1.0f, 1.0f, 1.0f };
	DirectX::XMFLOAT3 FresnelR0 = { 0.01f, 0.01f, 0.01f };
	float Roughness = .25f;
	DirectX::XMFLOAT4X4 MatTransform = MathHelper::Identity4x4();
	float ETA = 1.f;
	int DisplacementSrvHeapIndex = 0;
};

